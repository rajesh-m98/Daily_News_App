// Quick reference for making screens responsive with ScreenUtil
//
// Import: import 'package:flutter_screenutil/flutter_screenutil.dart';
//
// Usage:
// - Width: 16.w (instead of 16)
// - Height: 16.h (instead of 16)
// - Font Size: 16.sp (instead of 16)
// - Radius: 16.r (instead of 16)
//
// Examples:
// - EdgeInsets.all(16) → EdgeInsets.all(16.w)
// - SizedBox(height: 20) → SizedBox(height: 20.h)
// - fontSize: 14 → fontSize: 14.sp
// - BorderRadius.circular(12) → BorderRadius.circular(12.r)
//
// All screens and widgets have been updated with responsive design!
